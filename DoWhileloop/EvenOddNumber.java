package DoWhileloop;

public class EvenOddNumber {
    public static void main(String[] args) {
        int i = 2;
        System.out.println("Even numer:");
        do {
            System.out.println(i);
            i = i + 2;
        }
        while (i <= 20);
        int a = 1;
        System.out.println("Odd Number:");
        do {
            System.out.println(a);
            a = a + 2;
        }
        while (a <= 20);
    }
}







